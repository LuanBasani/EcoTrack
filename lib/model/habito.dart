// Classe que representa um hábito sustentável
class Habito {
  // Id do hábito
  final String id;

  // Nome/título do hábito
  final String titulo;

  // Ícone usado para representar o hábito
  final String icone;

  // Construtor da classe
  Habito({
    required this.id,
    required this.titulo,
    required this.icone,
  });
}